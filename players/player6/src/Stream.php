namespace Ouebsson\BattleShip;

final class Stream {

  public function read(): string {
    $message = fgets(STDIN);
    if($message === false) {
      die("error could not read STDIN\n");
    }
    return trim($message);
  }

  public function write(string $message): void {
    echo "$message\n";
  }
}